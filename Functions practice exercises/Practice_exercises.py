#A function that takes a name as input and prints a greeting message.
def greet(name):
    print(f"Hello, {name}!")
#A function that calculates the area of a rectangle given its length and width.
def area_of_rectangle(length, width):
    return length * width
#A function that checks if a number is even or odd and prints the result.
def is_even(number):
    if number % 2 == 0:
        print(f"{number} is even.")
    else:
        print(f"{number} is odd.")


